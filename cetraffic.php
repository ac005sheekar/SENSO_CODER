Complex intersections with multiple vehicle lanes, bike lanes, and crosswalks are common examples of traffic engineering projects<br><br><br>
Traffic engineering is a branch of civil engineering that uses engineering techniques to achieve the safe and efficient movement of people and goods on roadways. It focuses mainly on research for safe and efficient traffic flow, such as road geometry, sidewalks and crosswalks, cycling infrastructure, traffic signs, road surface markings and traffic lights. Traffic engineering deals with the functional part of transportation system, except the infrastructures provided.<br><br><br>
<br><br><br>
Traffic engineering is closely associated with other disciplines:<br><br><br>
<br><br><br>
Transport engineering<br><br><br>
Pavement engineering<br><br><br>
Bicycle transportation engineering<br><br><br>
Highway engineering<br><br><br>
Transportation planning<br><br><br>
Urban planning<br><br><br>
Human factors engineering.<br><br><br>
Typical traffic engineering projects involve designing traffic control device installations and modifications, including traffic signals, signs, and pavement markings. Examples of Engineering Plans include pole engineering analysis and Storm Water Prevention Programs (SWPP).[1] However, traffic engineers also consider traffic safety by investigating locations with high crash rates and developing countermeasures to reduce crashes. Traffic flow management can be short-term (preparing construction traffic control plans, including detour plans for pedestrian and vehicular traffic) or long-term (estimating the impacts of proposed commercial/residential developments on traffic patterns). Increasingly, traffic problems are being addressed by developing systems for intelligent transportation systems, often in conjunction with other engineering disciplines, such as computer engineering and electrical engineering.<br><br><br>
<br><br><br>

Traffic systems<br><br><br>
Traditionally, road improvements have consisted mainly of building additional infrastructure. However, dynamic elements are now being introduced into road traffic management. Dynamic elements have long been used in rail transport. These include sensors to measure traffic flows and automatic, interconnected, guidance systems to manage traffic (for example, traffic signs which open a lane in different directions depending on the time of day). Also, traffic flow and speed sensors are used to detect problems and alert operators, so that the cause of the congestion can be determined, and measures can be taken to minimize delays. These systems are collectively called intelligent transportation systems.<br><br><br>
<br><br><br>
Lane flow equation<br><br><br>

A ramp meter limits the rate at which vehicles can enter the freeway<br><br><br>
The relationship between lane flow (Q, vehicles per hour), space mean speed (V, kilometers per hour) and density (K, vehicles per kilometer) is<br><br><br>

{ Q=KV} Q=KV<br><br><br><br><br><br>

Observation on limited access facilities suggests that up to a maximum flow, speed does not decline while density increases. However, above a critical threshold, increased density reduces speed. Additionally, beyond a further threshold, increased density reduces flow as well.<br><br><br>

Therefore, speeds and lane flows at bottlenecks can be kept high during peak periods by managing traffic density using devices that limit the rate at which vehicles can enter the highway. Ramp meters, signals on entrance ramps that control the rate at which vehicles are allowed to enter the mainline facility, provide this function (at the expense of increased delay for those waiting at the ramps).<br><br><br>

Highway safety<br><br><br>
Highway safety engineering is a branch of traffic engineering that deals with reducing the frequency and severity of crashes. It uses physics and vehicle dynamics, as well as road user psychology and human factors engineering, to reduce the influence of factors that contribute to crashes. A well-drafted Traffic Control Plan (TCP) is critical to any job involving roadway work. A properly-prepared TCP will specify equipment, signage, placement, and personnel.[2]<br><br><br>

A typical traffic safety investigation follows these steps <br><br><br><br><br><br>

1. Identify and prioritize investigation locations. Locations are selected by looking for sites with higher than average crash rates, and to address citizen complaints.<br><br><br>
2. Gather data. This includes obtaining police reports of crashes, observing road user behavior, and collecting information on traffic signs, road surface markings, traffic lights <br><br><br>and road geometry.
3. Analyze data. Look for collisions patterns or road conditions that may be contributing to the problem.<br><br><br>
4. Identify possible countermeasures to reduce the severity or frequency of crashes.<br><br><br>
• Evaluate cost/benefit ratios of the alternatives<br><br><br>
• Consider whether a proposed improvement will solve the problem, or cause "crash migration." For example, preventing left turns at one intersection may eliminate left turn crashes at that location, only to increase them a block away.<br><br><br>
• Are any disadvantages of proposed improvements likely to be worse than the problem you are trying to solve?<br><br><br>
5. Implement improvements.<br><br><br>
6. Evaluate results. Usually, this occurs some time after the implementation. Have the severity and frequency of crashes been reduced to an acceptable level? If not, return to step 2.<br><br><br>
